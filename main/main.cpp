#include "misc.h"
#include "gt.h"
#include "mycomp.h"
//=============================================================================
extern "C" void app_main()
{
    initArduino();
    Serial.begin(115200);
    vTaskPrioritySet(NULL, 0);

    GT_Init();
    mycomp();

    while (true)
    {
        GT_Handler();
        GT_Poll();

        yield();
    }
}
